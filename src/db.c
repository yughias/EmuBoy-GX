db_hash db_search(u8* gamecode){
    for(int i = 0; i < sizeof(rom_database)/sizeof(rom_info_t); i++)
        if(!memcmp(rom_database[i].name, gamecode, 3))
            return rom_database[i].type;

    return 0xFF;
}

u16 db_get_id_code(db_hash hash){
    switch(hash){
        case 0x4: case 0x5:
        return 0x3D1F;

        case 0x6: case 0x7:
        return 0xD4BF;

        case 0x8: case 0x9:
        return 0x1B32;

        case 0xA: case 0xB:
        return 0x09C2;

        case 0xC: case 0xD:
        return 0x1362;

        default:
        return 0;
    }
}

size_t db_get_size(db_hash hash){
    switch(hash){
        case 0x0: case 0x1:
        return (1 << 9);

        case 0x2: case 0x3:
        return (1 << 13);

        case 0x4: case 0x5:
        case 0x6: case 0x7:
        case 0x8: case 0x9:
        return (1 << 16);

        case 0xA: case 0xB:
        case 0xC: case 0xD:
        return (1 << 17);

        case 0xE:
        return (1 << 15);

        default:
        return 0;
    }
}